#include <unistd.h>

char	*ft_strcpy(char *s1, char *s2)
{
	int	i;

	i = -1;
	if (!s2)
		return (NULL);
	while (s2[++i] != '\0')
		s1[i] = s2[i];
	s1[i] = '\0';
	return (s1);
}
